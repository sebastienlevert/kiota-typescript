import {AdditionalDataHolder, Parsable} from '@microsoft/kiota-abstractions';

export interface PrintCertificateSigningRequest extends Partial<AdditionalDataHolder>, Partial<Parsable> {
    /** Stores additional data not described in the OpenAPI description found when deserializing. Can be used for serialization as well. */
    additionalData?: Record<string, unknown>;
    /** A base64-encoded pkcs10 certificate request. Read-only. */
    content?: string;
    /** The OdataType property */
    odataType?: string;
    /** The base64-encoded public portion of an asymmetric key that is generated by the client. Read-only. */
    transportKey?: string;
}
